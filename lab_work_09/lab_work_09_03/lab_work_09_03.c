#include <stdio.h>
#include <math.h>

int main() {
	int n;      // число
	int i;
	int k = 0;  // кількість цифр у числі

	printf("Введіть число n: ");
	scanf("%d", &n);

	i = n;
	do {
		++k;
		i /= 10;
	} while (i != 0);

	printf("Кількість цифр у числі %d: %d\n", n, k);

	return 0;
}
